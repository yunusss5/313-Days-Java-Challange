public class if_elseif_else {
   public static void main(String[] args) {
    String Day="Monday";

    if (Day=="Sunday"){
        System.out.println("it's Weekend, Office is Close ");
    }
    else if (Day=="Monday"){
        System.out.println("it's Weekday, Office is Open ");
    }
    else if (Day=="Tuesday"){
        System.out.println("it's Weekday, Office is Open ");
    }
    else if (Day=="Wednesday"){
        System.out.println("it's Weekday, Office is Open ");
    }
    else if (Day=="Thursday"){
        System.out.println("it's Weekday, Office is Open ");
    }
    else if (Day=="Friday"){
        System.out.println("it's Weekday, Office is Open ");
    }
    else{
        System.out.println("Weekend, Come after Sunday!");
    }
   } 
}
